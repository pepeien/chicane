using System.Text;

namespace Reflector
{
    static class Emitter
    {
        public static string Emit(
            string originalPath,
            List<TypeModel> types,
            List<EnumModel> enums,
            Dictionary<string, string>? externalTypes = null,
            string? baseDir = null
        )
        {
            var sb = new StringBuilder();
            sb.AppendLine("// AUTO-GENERATED by Reflector — do not edit by hand");
            sb.AppendLine("#pragma once");
            sb.AppendLine();
            sb.AppendLine("#include \"Chicane/Core/Reflection/Enum/AutoRegister.hpp\"");
            sb.AppendLine("#include \"Chicane/Core/Reflection/Type/AutoRegister.hpp\"");
            sb.AppendLine();

            var includePath = baseDir != null
                ? Path.GetRelativePath(baseDir, originalPath).Replace('\\', '/')
                : Path.GetFileName(originalPath);

            sb.AppendLine($"#include \"{includePath}\"");
            sb.AppendLine();

            Dictionary<string, string> reflectedTypes = [];

            foreach (var t in types)
            {
                var qualified = string.IsNullOrWhiteSpace(t.Namespace)
                    ? t.Name
                    : $"{t.Namespace}::{t.Name}";

                reflectedTypes.TryAdd(t.Name, qualified);
                reflectedTypes.TryAdd(qualified, qualified);

                if (!string.IsNullOrWhiteSpace(t.Namespace))
                {
                    var nsParts = t.Namespace.Split("::");

                    for (int j = nsParts.Length - 1; j >= 0; j--)
                    {
                        var partial = string.Join("::", nsParts[j..]) + "::" + t.Name;

                        reflectedTypes.TryAdd(partial, qualified);
                    }
                }
            }

            if (externalTypes != null)
            {
                foreach (var kv in externalTypes)
                {
                    reflectedTypes.TryAdd(kv.Key, kv.Value);

                    var parts = kv.Value.Split("::");
                    for (int j = parts.Length - 1; j >= 1; j--)
                    {
                        var partial = string.Join("::", parts[j..]);
                        reflectedTypes.TryAdd(partial, kv.Value);
                    }
                }
            }

            foreach (var e in enums)
            {
                EmitEnum(sb, e);
            }

            foreach (var t in types)
            {
                EmitType(sb, t, reflectedTypes);
            }

            return sb.ToString();
        }

        static string QualifiedName(string? ns, string name)
        {
            if (string.IsNullOrWhiteSpace(ns))
            {
                return name;
            }

            return $"{ns}::{name}";
        }

        static string ResolveFieldType(string fieldTypeName, Dictionary<string, string> reflectedTypes)
        {
            if (reflectedTypes.TryGetValue(fieldTypeName, out var qualified))
            {
                return qualified;
            }

            if (fieldTypeName.Contains("::"))
            {
                return fieldTypeName;
            }

            return fieldTypeName;
        }

        static string SafeIdentifier(string qualifiedName)
            => qualifiedName.Replace("::", "_");

        static void EmitEnum(StringBuilder sb, EnumModel e)
        {
            var qualifiedName = QualifiedName(e.Namespace, e.Name);

            sb.AppendLine($"inline Chicane::ReflectionEnumAutoRegister _reg_enum_{SafeIdentifier(qualifiedName)}(");
            sb.AppendLine($"\t{{");
            sb.AppendLine($"\t\t\"{qualifiedName}\",");

            sb.AppendLine("\t\t{");
            foreach (var en in e.Enumerators)
            {
                sb.AppendLine($"\t\t\t{{");
                sb.AppendLine($"\t\t\t\t\"{en.Name}\",");
                sb.AppendLine($"\t\t\t\tstatic_cast<int>({qualifiedName}::{en.Name})");
                sb.AppendLine($"\t\t\t}},");
            }
            sb.AppendLine("\t\t}");

            sb.AppendLine("\t}");
            sb.AppendLine(");");
            sb.AppendLine();
        }

        static void EmitType(StringBuilder sb, TypeModel t, Dictionary<string, string> reflectedTypes)
        {
            var qualifiedName = QualifiedName(t.Namespace, t.Name);
            var safeId = SafeIdentifier(qualifiedName);

            sb.AppendLine("#if defined(__GNUC__) || defined(__clang__)");
            sb.AppendLine("#   pragma GCC diagnostic push");
            sb.AppendLine("#   pragma GCC diagnostic ignored \"-Winvalid-offsetof\"");
            sb.AppendLine("#endif");
            sb.AppendLine();

            sb.AppendLine($"inline Chicane::ReflectionTypeAutoRegister _reg_{safeId}(");
            sb.AppendLine($"\tChicane::ReflectionTypeInfo(");
            sb.AppendLine($"\t\t\"{qualifiedName}\",");
            sb.AppendLine($"\t\tsizeof({qualifiedName}),");
            sb.AppendLine($"\t\tstd::type_index(typeid({qualifiedName})),");

            sb.AppendLine("\t\t{");
            foreach (ConstructorModel c in t.Constructors)
            {
                var resolvedParams = c.ParamTypes
                    .Select(p => ResolveFieldType(p, reflectedTypes))
                    .ToList();

                var paramUnpack = string.Join(",", resolvedParams.Select((p, i) => $"\n\t\t\t\t\t\tstd::any_cast<{p}>(inParams.at({i}))"));

                if (c.ParamTypes.Count() > 0)
                {
                    paramUnpack += "\n\t\t\t\t\t";
                }

                sb.AppendLine(
                    $"\t\t\t[](std::vector<std::any> inParams)\n" +
                    $"\t\t\t{{\n" +
                    $"\t\t\t\tif (inParams.size() < {c.ParamTypes.Count()})\n" +
                    $"\t\t\t\t{{\n" +
                    $"\t\t\t\t\tthrow std::runtime_error(\"Missing reflected constructor [{qualifiedName}] parameters [{string.Join(",", resolvedParams.Select((p, i) => $"{p}"))}]\");\n" +
                    $"\t\t\t\t}}\n" +
                    $"\t\n" +
                    $"\t\t\t\treturn static_cast<void*>(\n" +
                    $"\t\t\t\t\tnew {qualifiedName}({paramUnpack})\n" +
                    $"\t\t\t\t);\n" +
                    $"\t\t\t}},"
                );
            }
            sb.AppendLine("\t\t},");

            sb.AppendLine("\t\t{");
            foreach (MethodModel m in t.Methods)
            {
                var resolvedParams = m.ParamTypes
                    .Select(p => ResolveFieldType(p, reflectedTypes))
                    .ToList();

                var paramUnpack = string.Join(",", resolvedParams.Select((p, i) => $"\n\t\t\t\t\t\tstd::any_cast<{p}>(inParams.at({i}))"));

                if (m.ParamTypes.Count() > 0)
                {
                    paramUnpack += "\n\t\t\t\t\t";
                }

                sb.AppendLine(
                    $"\t\t\t{{\n" +
                    $"\t\t\t\t\"{m.Name}\",\n" +
                    $"\t\t\t\t\"{m.ReturnType}\",\n" +
                    $"\t\t\t\t{{{string.Join(" ,", m.ParamTypes.Select(p => $"\"{p}\""))}}},\n" +
                    $"\t\t\t\t[](void* inInstance, std::vector<std::any> inParams)\n" +
                    $"\t\t\t\t{{\n" +
                    $"\t\t\t\t\tif (inParams.size() < {m.ParamTypes.Count()})\n" +
                    $"\t\t\t\t\t{{\n" +
                    $"\t\t\t\t\t\tthrow std::runtime_error(\"Missing reflected method [{m.Name}] parameters [{string.Join(",", resolvedParams.Select((p, i) => $"{p}"))}]\");\n" +
                    $"\t\t\t\t\t}}\n" +
                    $"\t\n" +
                    $"\t\t\t\t\tstatic_cast<{qualifiedName}*>(inInstance)->{m.Name}({paramUnpack});\n" +
                    $"\t\t\t\t}}\n" +
                    $"\t\t\t}},"
                );
            }
            sb.AppendLine("\t\t},");

            sb.AppendLine("\t\t{");
            foreach (FieldModel f in t.Fields)
            {
                if (f.Names.Count <= 0)
                {
                    continue;
                }

                var resolvedType = ResolveFieldType(f.TypeName, reflectedTypes);
                bool isReflected = reflectedTypes.ContainsKey(f.TypeName) || reflectedTypes.ContainsValue(f.TypeName);

                sb.AppendLine(
                    $"\t\t\t{{\n" +
                    $"\t\t\t\t{{{string.Join(" ,", f.Names.Select(p => $"\"{p}\""))}}},\n" +
                    $"\t\t\t\t\"{resolvedType}\",\n" +
                    $"\t\t\t\toffsetof({qualifiedName}, {f.Name}),\n" +
                    $"\t\t\t\tsizeof({(f.IsPointer ? "void*" : resolvedType)}),\n" +
                    $"\t\t\t\tstd::type_index(typeid({resolvedType})),\n" +
                    $"\t\t\t\t{(isReflected ? "true" : "false")},\n" +
                    $"\t\t\t\t{(f.IsPointer ? "true" : "false")}\n" +
                    $"\t\t\t}},"
                );
            }
            sb.AppendLine("\t\t}");
            sb.AppendLine("\t)");
            sb.AppendLine(");");
            sb.AppendLine();

            sb.AppendLine("#if defined(__GNUC__) || defined(__clang__)");
            sb.AppendLine("#   pragma GCC diagnostic pop");
            sb.AppendLine("#endif");
            sb.AppendLine();
        }
    }
}